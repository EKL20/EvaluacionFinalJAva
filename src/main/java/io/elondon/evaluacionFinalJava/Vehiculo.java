package io.elondon.evaluacionFinalJava;

public class Vehiculo {
    private int triupulacion;
    private  int pesoVacio;
    private int pesoUtil;

    public int getTriupulacion() {
        return triupulacion;
    }

    public void setTriupulacion(int triupulacion) {
        this.triupulacion = triupulacion;
    }

    public int getPesoVacio() {
        return pesoVacio;
    }

    public void setPesoVacio(int pesoVacio) {
        this.pesoVacio = pesoVacio;
    }

    public int getPesoUtil() {
        return pesoUtil;
    }

    public void setPesoUtil(int pesoUtil) {
        this.pesoUtil = pesoUtil;
    }
}
